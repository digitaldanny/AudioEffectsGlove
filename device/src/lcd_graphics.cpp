bool LcdGfx::init()
{
    lcd_setup();
    return true;
}

/*
 * +-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+
 * DESCRIPTION: LcdGfx::writeDataBlock
 * Initialize GPIO to control the mux selects.
 *
 * NOTE - This function belongs in the cfa6448A_lcd.cpp file, but I am placing it
 * here to minimize changing the 3rd party libraries as much as possible.
 *
 * INPUTS:
 * @data - Pointer to buffer to transfer.
 * @len - Length of the data buffer.
 *
 * RETURN: True if transfer was successful.
 * +-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+
*/
bool LcdGfx::writeDataBlock(unsigned char* data, uint16_t len)
{
    // Select the LCD's data register
    SET_RS;

    // Select the LCD controller
    CLR_CS;

    for(uint16_t i = 0; i < len; i++)
    {
      //Read this byte from the program memory / flash,
      //Send the data via SPI:
      SPI.transfer(data[i]);
    }

    // Deselect the LCD controller
    SET_CS;
    return true;
}

/*
 * +-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+
 * DESCRIPTION: LcdGfx::drawString
 * Draws input message to a specified position on the screen.
 *
 * INPUTS:
 * @x - Starting column in character widths (6 for small font).
 * @y - Starting row in memory pages (8 lines each).
 * @msg - Pointer to message to write.
 * @msgLen - Number of characters in the message.
 *
 * RETURN: True if transfer was successful.
 * +-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+
*/
bool LcdGfx::drawString(uint8_t x, uint8_t y, char* msg, uint16_t msgLen)
{
    unsigned char *s;

    Set_Address(x*6, y);
    if (msgLen + x > 21) msgLen = 21 - x;
    for (uint16_t i = 0; i < msgLen; i++)
    {
        s = &ucSmallFont[(unsigned char)msg[i]*6];
        writeDataBlock(s, 6);
    }
    return true;
}

/*
 * +-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+
 * DESCRIPTION: LcdGfx::drawFill
 * Fills entire LCD with a byte pattern (0x00 for all off, 0xFF for all on).
 *
 * INPUTS:
 * @pattern - Pattery to fill LCD with.
 *
 * RETURN: True if transfer was successful.
 * +-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+-----+
*/
#define NUM_LINES   8
#define NUM_COLS    8
unsigned char patternBuf[16*NUM_COLS];
bool LcdGfx::drawFill(unsigned char pattern)
{
    memset(patternBuf, pattern, 16*NUM_COLS);

    for (uint8_t y = 0; y < NUM_LINES; y++)
    {
        Set_Address(0, y); // set to (0,Y)
        writeDataBlock(patternBuf, 16*NUM_COLS); // fill with data byte
    } // for y
    return 0;
}
